let toDoList = ["Wake up", "Brush teeth", "Have a breakfast"];
//printing the orignal array
console.log(toDoList);
// Output: ["Wake up", "Brush teeth", "Have a breakfast"]


// adding a new task and printing an updated array
toDoList.push("Go for a walk")
console.log(toDoList);
// Output: ["Wake up", "Brush teeth", "Have a breakfast", "Go for a walk"]


// Removing the last task from an array and prinitng an updated array
toDoList.pop();
console.log(toDoList);
// Output: ["Wake up", "Brush teeth", "Have a breakfast"]
